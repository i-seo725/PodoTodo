import UIKit

extension UIFont {
    
    class var jamsilNav: UIFont { return UIFont(name: Font.jamsilRegular.rawValue, size: 16)! }
    class var jamsilTitle: UIFont { return UIFont(name: Font.jamsilRegular.rawValue, size: 14)! }
    class var jamsilSubTitle: UIFont { return UIFont(name: Font.jamsilRegular.rawValue, size: 13)! }
    class var jamsilContent: UIFont { return UIFont(name: Font.jamsilLight.rawValue, size: 12)! }
    
}
